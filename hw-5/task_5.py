# # 1. დაწერეთ პითონის პროგრამა, რომელიც დასაწყისში შექმნის ცარიელ სიას ([]), თუ მომხარებელი შეიყვანს სიმბოლო 'a'-ს, ნიშნავს რომ უნდა დაამატოთ სიაში რიცხვი; თუ აკრიფა 'r', სიიდან უნა წაიშალოს რიცხვი; 'e'-ს შეტანისას პროგრამამ უნდა დაასრულოს მუშაობა. მიღებული შედეგი დაბეჭდეთ კონსოლში.
# a – append; # r – remove; # e – exit - გამოიყენეთ მხოლოდ ეს ბრძანებები და მოახდინეთ სიაზე ზემოქმედება.

my_list = []

while True:
    order = input(
        "თუ გინდა სიაში დამატება შეიყვანა - a\nთუ გინდა სიიდან წაშლა შეიყვანე r\nთუ გინდა პროგრამიდან გამოსვლა შეიყვანე e: "
    ).strip()

    if order == "a":
        my_list.append(int(input("Enter number: ")))
        print("my_list =", my_list)
        print()

    elif order == "r":
        if len(my_list) == 0:
            print("სია ცარიელია!")
            print()
        else:
            remove_number = int(input("Enter number: "))
            if remove_number in my_list:
                my_list.remove(remove_number)
                print("my_list =", my_list)
                print()
            else:
                print("აღნიშნული რიცხვი არ იძებნება სიაში!")
                print()

    elif order == "e":
        print("პროგრამა დასრულებულია!")
        print()
        break
    else:
        print("არასწორი ბრძანება! შეიყვანე მითითებული რიცხვი")
        print()

