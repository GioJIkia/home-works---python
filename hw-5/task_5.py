# 3. დაწერეთ პითონის პროგრამა, რომელიც მიიღებს ტელეფონის ნომერს და regex-ით შეამოწმებს შეყვანილი ნომერი იცავს თუ არა "(123) 456-789" ფორმატს,
# თუ იცავს დააბრუნეთ შეყვანილი ტელეფონის ნომერი, წინააღმდეგ შემთხვევაში გამოიტანეთ "Invalid format" ტექსტი. მინიშნება: სრული დამთხვევისთვის გამოიყენეთ .fullmatch() მეთოდი re მოდულიდან.

import re

pattern = r"\(\d{3}\)\s\d{3}-\d{3}"

while True:
    phone = input("შეიყვანეთ ტელეფონის ნომერი შემდეგი ფორმატით (xxx) xxx-xxx: ").strip()

    if re.fullmatch(pattern, phone):
        print("ტელეფონის ნომერი:", phone)
        break
    else:
        if phone.isalpha():
            print("გთხოვთ შეიყვანოთ მხოლოდ ციფრები დადგენილი ფორმატით")
        elif len(phone) == 0:
            print("რიცხვები არ შეგიყვანიათ")
        else:
            print("არასწორი ფორმატითაა ტელეფონის ნომერი შეყვანილი")
